--Creacion de Tablas
CREATE TABLE TELEFONO (
  ID_TELEFONO VARCHAR2(50),
  NUMERO VARCHAR2(100),
  PRIMARY KEY (ID_TELEFONO)
);

CREATE TABLE CORREO (
  ID_CORREO VARCHAR2(50),
  EMAIL VARCHAR2(100),
  PRIMARY KEY (ID_CORREO)
);

CREATE TABLE FIDELIZACION (
  ID_FIDELIZACION VARCHAR2(50),
  DETALLE VARCHAR2(100),
  PRIMARY KEY (ID_FIDELIZACION)
);

CREATE TABLE DIRECCION (
  ID_DIRECCION VARCHAR2(50),
  DETALLE VARCHAR2(100),
  PRIMARY KEY (ID_DIRECCION)
);

CREATE TABLE PROVINCIA (
  ID_PROVINCIA VARCHAR2(50),
  NOMBRE VARCHAR2(100),
  PRIMARY KEY (ID_PROVINCIA)
);

CREATE TABLE CANTON (
  ID_CANTON VARCHAR2(50),
  ID_PROVINCIA VARCHAR2(50),
  NOMBRE VARCHAR2(100),
  PRIMARY KEY (ID_CANTON),
  FOREIGN KEY (ID_PROVINCIA) REFERENCES PROVINCIA(ID_PROVINCIA)
);

CREATE TABLE DISTRITO (
  ID_DISTRITO VARCHAR2(50),
  ID_CANTON VARCHAR2(50),
  NOMBRE VARCHAR2(100),
  PRIMARY KEY (ID_DISTRITO),
  FOREIGN KEY (ID_CANTON) REFERENCES CANTON(ID_CANTON)
);

CREATE TABLE USUARIO (
  ID_USUARIO VARCHAR2(50),
  ID_TIPO_USUARIO VARCHAR2(50),
  NOMBRE VARCHAR2(100),
  APELLIDO VARCHAR2(100),
  PRIMARY KEY (ID_USUARIO),
  FOREIGN KEY (ID_TIPO_USUARIO) REFERENCES TIPO_USUARIO(ID_TIPO_USUARIO)
);

CREATE TABLE TIPO_USUARIO (
  ID_TIPO_USUARIO VARCHAR2(50),
  DESCRIPCION VARCHAR2(100),
  PRIMARY KEY (ID_TIPO_USUARIO)
);

CREATE TABLE EMPLEADO (
  ID_EMPLEADO VARCHAR2(50),
  ID_PUESTO VARCHAR2(50),
  ID_USUARIO VARCHAR2(50),
  PRIMARY KEY (ID_EMPLEADO),
  FOREIGN KEY (ID_PUESTO) REFERENCES PUESTO(ID_PUESTO),
  FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE FACTURACION (
  ID_FACTURA VARCHAR2(50),
  ID_EMPLEADO VARCHAR2(50),
  ID_CAJA VARCHAR2(50),
  ID_USUARIO VARCHAR2(50),
  FECHA VARCHAR2(100),
  MONTO_TOTAL VARCHAR2(100),
  PRIMARY KEY (ID_FACTURA),
  FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO(ID_EMPLEADO),
  FOREIGN KEY (ID_CAJA) REFERENCES CAJA(ID_CAJA),
  FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)
);

CREATE TABLE CATEGORIA (
  ID_CATEGORIA VARCHAR2(50),
  NOMBRE_CATEGORIA VARCHAR2(100),
  PRIMARY KEY (ID_CATEGORIA)
);

CREATE TABLE PRODUCTO (
  ID_PRODUCTO VARCHAR2(50),
  ID_CATEGORIA VARCHAR2(50),
  NOMBRE VARCHAR2(100),
  PRECIO VARCHAR2(100),
  STOCK VARCHAR2(100),
  PRIMARY KEY (ID_PRODUCTO),
  FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(ID_CATEGORIA)
);

CREATE TABLE REABASTECIMIENTO (
  ID_REABASTECIMIENTO VARCHAR2(50),
  ID_PRODUCTO VARCHAR2(50),
  FECHA VARCHAR2(100),
  CANTIDAD VARCHAR2(100),
  PRIMARY KEY (ID_REABASTECIMIENTO),
  FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO)
);

CREATE TABLE PUESTO (
  ID_PUESTO VARCHAR2(50),
  NOMBRE_PUESTO VARCHAR2(100),
  PRIMARY KEY (ID_PUESTO)
);

CREATE TABLE SALARIOS (
  ID_SALARIO VARCHAR2(50),
  ID_EMPLEADO VARCHAR2(50),
  MONTO VARCHAR2(100),
  FECHA VARCHAR2(100),
  PRIMARY KEY (ID_SALARIO),
  FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO(ID_EMPLEADO)
);

CREATE TABLE REPORTE (
  ID_REPORTE VARCHAR2(50),
  ID_FACTURA VARCHAR2(50),
  DETALLE VARCHAR2(100),
  PRIMARY KEY (ID_REPORTE),
  FOREIGN KEY (ID_FACTURA) REFERENCES FACTURACION(ID_FACTURA)
);

CREATE TABLE CAJA (
  ID_CAJA VARCHAR2(50),
  NOMBRE_CAJA VARCHAR2(100),
  PRIMARY KEY (ID_CAJA)
);

CREATE TABLE TIENDA (
  ID_TIENDA VARCHAR2(50),
  NOMBRE_TIENDA VARCHAR2(100),
  DIRECCION VARCHAR2(100),
  PRIMARY KEY (ID_TIENDA)
);

CREATE TABLE INVENTARIO (
  ID_INVENTARIO VARCHAR2(50),
  ID_PRODUCTO VARCHAR2(50),
  CANTIDAD VARCHAR2(100),
  FECHA_ACTUALIZACION VARCHAR2(100),
  PRIMARY KEY (ID_INVENTARIO),
  FOREIGN KEY (ID_PRODUCTO) REFERENCES PRODUCTO(ID_PRODUCTO)
);

